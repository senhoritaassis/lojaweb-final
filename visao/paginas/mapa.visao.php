<h2> ESTÚDIOS DO CINEMA E DA TV</h2>
<p>Ama ver conteúdos inéditos? Os maiores estúdios de Hollywood invadem a CCXP para trazer trailers especiais, cenas exclusivas e os astros dos filmes do momento. Em 2018, a Sony exibiu parte da animação Homem-Aranha: No Aranhaverso, a Netflix exibiu o teaser da terceira temporada de Stranger Things e rolou pré-estreias exclusivas de Aquaman e Creed (Warner) e Wi-fi Ralph (Disney).</p>



<h2>VEJA SEU ARTISTA FAVORITO DE PERTO</h2>
<p>Imagina ficar coladinho com o seu ídolo. Na CCXP você pode conhecer os principais atores, diretores e produtores de seus filmes, games e séries de TV favoritos. Muitos nomes consagrados já passaram por nossos auditórios, e em 2018, marcaram presença astros de Hollywood como Sandra Bullock, Andy Serkis, Brie Larson, Zachary Levi, Sophie Turner e muitos outros.</p>



<h2>ESTANDES DE TIRAR O FÔLEGO</h2>
<p>Grande parte dos principais estúdios, marcas e empresas que a gente ama participam da CCXP. Todos os anos são montados espaços com produtos licenciados proporcionando experiências divertidas, tais como a Casa da moeda de La Casa de Papel, a lanchonete Pop’s de Riverdale e pela primeira vez na história, uma reprodução da cabana do Hagrid fora dos estúdios Warner e da Universal Studios.</p>


<h2>MESTRES E ARTISTAS DOS QUADRINHOS</h2>
<p>O Artists' Alley é de fazer qualquer fã da nona arte pirar! É o coração da CCXP e conta com centenas de mesas para ilustradores, roteiristas, coloristas e artistas gráficos comercializarem seus trabalhos, autografarem e interagirem com os fãs. Nomes incríveis já passaram pela CCXP, tais como Romita Jr., Gail Simone, Arthur Adams, John Cassaday,David Lloyd, David Mack, Denys Cowan, Brian Azzarello, Eduardo Risso, Yanick Paquette, Simon Bisley, Mark Waid, Bill Sienkiewicz, Glenn Fabry, Scott Snyder, Fábio Moon e Gabriel Bá, Roger Cruz, Mike Deodato, entre muitos outros.</p>


<h2>AUDITÓRIOS ÉPICOS</h2>
<p>A história da cultura pop é feita nos nossos auditórios: grandes anúncios, trailers, conteúdos exclusivos, curiosidades das maiores franquias do cinema, games, quadrinhos e muito mais! O Auditório Thunder Cinemark XD possui a maior sala de cinema indoor da América Latina, em 2018 o espaço contou com mais de 3 mil lugares e projeção com tecnologia de ponta e altíssima resolução. Neste mesmo ano, aconteceu a pré-estreia dos filmes Wi-fi Ralph, Creed II e Aquaman e assistimos também, em primeira mão, o trailer de Homem-Aranha: de volta para a casa na presença de Tom Holland, Jacob Batalon e Jake Gyllenhaal. Nos auditórios Ultra e Prime, são realizados bate-papos com artistas, workshops e outras atividades imperdíveis.</p>


<h2>O MAIOR SHOPPING GEEK DO MUNDO</h2>
<p>Na CCXP você vai encontrar lançamentos, edições exclusivas (à venda somente no evento) e as últimas novidades para fazerem a alegria de qualquer fã. A CCXP acontece poucas semanas antes do Natal, portanto é o lugar perfeito para comprar presentes especiais.</p>


<h2>COLECIONÁVEIS QUE VOCÊ SÓ ENCONTRA AQUI</h2>
<p>É a sua chance de conseguir itens exclusivos que deixarão sua coleção ainda mais épica. A CCXP é o lugar ideal para encontrar colecionáveis em seus diversos estandes com produtos importados e nacionais para todos os bolsos.</p>


<h2>COSPLAYERS ÉPICOS</h2>
<p>Desde a primeira edição da CCXP, o evento se firmou como o ambiente ideal para cosplayers expressarem seu hobby e sua arte, além de promover desfiles e o único concurso do mundo julgado com o auxílio de inteligência artificial. Venha conhecer e tirar fotos com esses cosplayers incríveis. Melhor ainda: venha customizado como seu personagem favorito e participe dessa festa.</p>


<h2>EXPERIÊNCIA EM SÃO PAULO</h2>
<p>A CCXP é um evento para fãs de todas as idades, onde você pode curtir o que mais gosta na companhia de outras pessoas igualmente apaixonadas. O clima é incrível e a energia está sempre nas alturas. A praça de alimentação é gigante, repleta de opções para todos os gostos, inclusive para quem tem restrições alimentares, e não há venda de bebidas alcoólicas dentro da CCXP. Os corredores são largos para que você possa se deslocar com facilidade. E fique à vontade para trazer crianças, inclusive de colo: temos um fraldário equipado para atendê-los com conforto e segurança.</p>

<h2>A MAIOR DO MUNDO É NOSSA</h2>
<p>Em 2018 a CCXP se firmou como o maior festival de cultura pop do mundo, pelo terceiro ano consecutivo. Foram mais de 262 mil pessoas aproveitando mais de 115 mil m² de atrações. Tudo isso no Brasil. Não dá pra perder!</p>
